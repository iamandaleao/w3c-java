// 6. Concatenar (juntar textos e variáveis)

class Concatenar {
    public static void main(String[] args) {
        String nome = "Leão";
        int idade = 20;

        System.out.println("Nome: " + nome);
        System.out.println("Idade: " + idade);
    }
}

