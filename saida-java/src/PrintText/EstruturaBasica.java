package PrintText;// 1. Estrutura mínima de um programa Java:

public class EstruturaBasica {
    public static void main(String[] args) {
        System.out.println("Seu texto vai aqui!");
    }
}


// public class Main { ... } → toda vez que for escrever código em Java, precisa estar dentro de uma classe. O nome “Main” pode mudar, mas normalmente se usa esse nos exemplos iniciais.
// public static void main(String[] args) { ... } → é o “portão de entrada”. O Java só começa a rodar o programa a partir daqui.
// System.out.println("..."); → é o comando que mostra texto na tela. O que estiver entre aspas aparece.


